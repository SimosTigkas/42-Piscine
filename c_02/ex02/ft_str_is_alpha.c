int	ft_str_is_alpha(char *str)
{
	int		i;
	int		b;
	char	c;

	c = '0';
	i = 0;
	b = 1;
	while (*(str + i) != '\0')
	{
		c = *(str + i);
		if (c < 65 || (c > 90 && c < 97) || c > 122)
		{
			b = 0;
		}
		i++;
	}
	return (b);
}
