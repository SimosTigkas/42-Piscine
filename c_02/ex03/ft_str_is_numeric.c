int	ft_str_is_numeric(char *str)
{
	int	i;
	int	b;
	int	c;

	i = 0;
	b = 1;
	while (*(str + i) != '\0')
	{
		c = *(str + i);
		if (c < 48 || c > 57)
		{
			b = 0;
		}
		i++;
	}
	return (b);
}
