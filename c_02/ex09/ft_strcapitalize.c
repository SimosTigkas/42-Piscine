char	*ft_strcapitalize(char *str)
{
	int		i;
	int		g;

	i = 0;
	g = 1;
	while (*(str + i) != '\0')
	{
		if (*(str + i - 1) == ' ')
			g = 1;
		if (g == 1)
		{
			if (*(str + i) >= 'a' && *(str + i) <= 'z')
				str[i] -= 32;
			g = 0;
		}
		else if (*(str + i) >= 'A' && *(str + i) <= 'Z')
		{
			str[i] += 32;
		}
		i++;
	}
	return (str);
}	
