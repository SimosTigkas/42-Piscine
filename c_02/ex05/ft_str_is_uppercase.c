int	ft_str_is_uppercase(char *str)
{
	int		i;
	int		b;
	char	c;

	i = 0;
	b = 1;
	while (*(str + i) != '\0')
	{
		c = *(str + i);
		if (c < 65 || c > 90)
		{
			b = 0;
		}
		i++;
	}
	return (b);
}
