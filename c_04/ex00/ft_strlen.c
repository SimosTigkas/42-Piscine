int	ft_strlen(char *str)
{
	int	i;

	i = 0;
	while (*(str + i) != '\0')
		i++;
	return (i);
}

int main(void)
{
	char *s1 = "Simos";

	printf("%d" , ft_strlen(s1));
	return (0);
}
